


int main()
{
    int k;
    printf("ボールは何個ありますか? ");
    scanf("%d", &k);

    if (k >= 100)
    {
        printf("たくさんのボール\n");
    }
    else if (k == 0)
    {
        printf("ボールはどこにやったの?\n");
    }
    else
    {
        printf("少しのボール\n");
    }

    return 0;
}


